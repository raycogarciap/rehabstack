// Função para processar cada arquivo
function fixDiacriticsInFile(filePath, languageKey) {
  try {
    // Lê o arquivo original
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    // Substitui completamente o bloco creator
    data.creator = creatorBlocks[languageKey];

    // Escreve de volta com indentação de 2 espaços
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');

    console.log(`✓ Arquivo corrigido: ${filePath}`);
    return true;
  } catch (error) {
    console.error(`✗ Erro ao processar ${filePath}:`, error.message);
    return false;
  }
}

