const localeFiles = ['en', 'es', 'pt', 'fr', 'de', 'ar'];

localeFiles.forEach(locale => {
  const filePath = path.join(messagesDir, `${locale}.json`);

  // Leer el archivo existente
  const raw = fs.readFileSync(filePath, 'utf-8');
  const data = JSON.parse(raw);

  // Verificar si ya existe la clave creator
  if (data.creator) {
    console.log(`[${locale}.json] Ya existe la clave "creator". Sobrescribiendo...`);
  }

  // Agregar el bloque creator
  data.creator = translations[locale];

  // Escribir de vuelta con formato legible (2 espacios de indentación)
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
  console.log(`[${locale}.json] Bloque "creator" agregado correctamente.`);
});

